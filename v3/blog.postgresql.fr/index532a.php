    <content type="html">    &lt;p&gt;Bonjour à toutes et à tous&amp;nbsp;!&lt;/p&gt;


&lt;p&gt;En automne dernier, l'association PostgreSQLFR a lancé une grande étude
pour mieux connaitre les utilisateurs de PostgreSQL et les
environnements dans lesquels ils évoluent.&lt;/p&gt;


&lt;p&gt;L'objectif de cette étude est de produire un document de synthèse de
quelques pages. Appelé (un peu pompeusement) &quot;livre blanc&quot;, ce document
aura pour but de mieux faire connaître PostgreSQL et donner des éléments
de décision aux personnes qui hésiteraient encore à se ranger du coté
de l'éléphant bleu.&lt;/p&gt;


&lt;p&gt;En deux mots, il s'agit de casser l'image de SGBD élitiste et
confidentiel que PostgreSQL traîne depuis des années.&lt;/p&gt;


&lt;p&gt;Cette grande étude, qui va durer 9 mois, comprend un questionnaire adressé
à tous les utilisateurs de PostgreSQL. Ce questionnaire est anonyme, il
contient 15 items. Il suffit de 3 minutes pour y répondre.&lt;/p&gt;


&lt;p&gt;N'hésitez pas à prendre ces quelques minutes et témoigner de votre
utilisation de PostgreSQL. Ces renseignements nous seront très utiles&amp;nbsp;!&lt;/p&gt;


&lt;p&gt;Voici l'adresse du questionnaire&amp;nbsp;:
&lt;a href=&quot;http://enquetes.postgresql.fr/index.php?sid=39543&amp;amp;lang=fr&quot; title=&quot;http://enquetes.postgresql.fr/index.php?sid=39543&amp;amp;lang=fr&quot;&gt;http://enquetes.postgresql.fr/index...&lt;/a&gt;&lt;/p&gt;


&lt;p&gt;Vous pouvez également transmettre l'adresse de cette enquête à toute
personne qui pourrait y répondre.&lt;/p&gt;


&lt;p&gt;Note aux personnes présentes lors du PGDay 2008 à Toulouse&amp;nbsp;: Si vous
avez déjà répondu au questionnaire qui vous a été transmis lors de la
conférence, votre témoignage a déjà été enregistré&amp;nbsp;!&lt;/p&gt;


&lt;p&gt;Si vous souhaitez nous aider dans cette étude, n'hésitez pas à nous
rejoindre. La page du projet se trouve ici&amp;nbsp;:&lt;/p&gt;


&lt;p&gt;&lt;a href=&quot;http://www.postgresql.fr/livre_blanc:&quot; title=&quot;http://www.postgresql.fr/livre_blanc:&quot;&gt;http://www.postgresql.fr/livre_blan...&lt;/a&gt;&lt;/p&gt;


&lt;p&gt;Enfin sachez que PostgreSQLFR est toujours à la recherche de personnes
qui acceptent de témoigner publiquement de leur utilisation de
PostgreSQL. Ce genre de témoignages est particulièrement important dans
la mesure où les cas d'utilisations concrets donnent confiance à
d'autres utilisateurs et cela accroît ainsi la communauté.&lt;/p&gt;


&lt;p&gt;Voici comment témoigner publiquement&amp;nbsp;:&lt;/p&gt;


