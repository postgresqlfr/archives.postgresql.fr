Les &lt;a href=&quot;http://fr.wikipedia.org/wiki/Aficionados&quot; target=&quot;_blank&quot;&gt;aficionados&lt;/a&gt; de PostgreSQL ne pourront que déplorer la suppression de cette fonctionnalité, bien qu'en définitive, elle ne soit que très rarement utilisée.
