    <id>urn:md5:973b3048909c6162528dbc0794be2604</id>
    <published>2013-09-29T18:03:00+01:00</published>
    <updated>2013-09-29T18:08:25+01:00</updated>
    <author><name>Gautier Di Folco</name></author>
        <dc:subject>Événements</dc:subject>
            
    <content type="html">    &lt;p&gt;Depuis 2008, PG Day France est l’événement phare de la communauté francophone de PostgreSQL. Une journée de conférence où les utilisateurs de PostgreSQL peuvent rencontrer les développeurs, les entreprises et les chercheurs qui font vivre ce logiciel.&lt;/p&gt;


&lt;p&gt;PG Day France est un événement itinérant qui change de paysage chaque année. Après Toulouse, Paris, Lyon et Nantes le comité d'organisation est à la recherche d'une nouvelle destination pour l'édition 2014.&lt;/p&gt;


&lt;p&gt;L'événement est prévu &lt;strong&gt;en juin 2014&lt;/strong&gt;. L'objectif est de rassembler 200 personnes pendant 2 jours.&lt;/p&gt;


&lt;p&gt;Si vous pensez que votre ville peut accueillir le PG Day France 2014, cet appel est fait pour vous&amp;nbsp;!&lt;/p&gt;



&lt;h2&gt;Qui peut se porter candidat&amp;nbsp;?&lt;/h2&gt;


&lt;p&gt;L'appel est ouvert à tous et à toutes, quel que soit le niveau de connaissance sur PostgreSQL. Nous cherchons avant tout une ou plusieurs personnes motivées pour nous faire découvrir leur ville.&lt;/p&gt;



&lt;p&gt;Si vous êtes sélectionné votre rôle principal sera de coordonner sur place les différents éléments/acteurs (salle, traiteur, plans du site, réception des goodies, etc.). Vous serez aidé par l'équipe du PG Day France, ainsi vous ne serez jamais seul.&lt;/p&gt;


&lt;h2&gt;Le dossier de candidature&lt;/h2&gt;


&lt;p&gt;Le dossier de candidature n'a pas besoin d'être très long, mais plus il sera détaillé et plus le comité de sélection y accordera de l'intérêt.&lt;/p&gt;


&lt;p&gt;Au minimum, nous vous demandons de nous fournir les informations suivantes&amp;nbsp;:&lt;/p&gt;


&lt;pre&gt; - Le nom de la ville,
 - Une présentation du (ou des) porteur(s) de la candidature,
 - Une liste de dates possibles,
 - Une liste de salles pouvant accueillir 200 personnes pendant 2 jours
   * par exemple: une université, un centre des congrès, etc.
   * penser à indiquer les tarifs de chaque lieu
 - Une liste d'hôtels de milieu de gamme disponibles (2 ou 3 étoiles)
   * indiquer si possible la capacité et les tarifs
 - Une liste de traiteurs
    * indiquer si possible les tarifs
 - Un lieu disponible pour expédier/stocker le matériel de la conférence (flyers, etc.).  
    * Par exemple, les locaux d'une entreprise.&lt;/pre&gt;



&lt;p&gt;En complément, vous pouvez également ajouter toute information que vous jugerez utile. Par exemple&amp;nbsp;:&lt;/p&gt;


&lt;pre&gt; * Préciser combien de bénévoles locaux pourront nous aider sur place,
 * Préciser s'il y a un GUL ou association pro-libre dans la ville,
 * Lister les entreprise locales qui pourraient s'associer à l'événement,
 * Décrire l'accès à la ville via les différents moyens de déplacement (avion, train, etc.).&lt;/pre&gt;


&lt;h2&gt;Dépôt de candidatures&lt;/h2&gt;


&lt;p&gt;Tous les dossiers doivent être envoyés à l'adresse &lt;a href=&quot;mailto:%63%6f%6e%74%61%63%74%40%70%67%64%61%79%2e%66%72&quot;&gt;contact@pgday.fr&lt;/a&gt; avant le 31 octobre 2013 à 23h59 CEST.&lt;/p&gt;


&lt;p&gt;Ensuite, le comité de sélection étudiera toutes les propositions valides. Le choix de la ville sera basé sur la présentation du dossier, la qualité des lieux proposés, leur intérêt pour une audience professionnelle et la cohérence de l'ensemble.&lt;/p&gt;


&lt;p&gt;La décision du comité de sélection sera finale et sans appel.&lt;/p&gt;


&lt;p&gt;Le comité de sélection est composé de membres de l'association PostgreSQLFr et de volontaires ayant déjà participé à l'organisation des PG Day France. Les membres du comité s'expriment en leur nom propre. Leurs choix ne reflètent pas la position de leur employeur.&lt;/p&gt;


&lt;p&gt;Les membres du comité de sélection ne peuvent pas être candidats.&lt;/p&gt;


&lt;h2&gt;Contact et Calendrier&lt;/h2&gt;


&lt;p&gt;Le calendrier du comité de sélection est le suivant&amp;nbsp;:&lt;/p&gt;


&lt;pre&gt; * 8 septembre : lancement de l'appel à candidature
 * 31 octobre  : date limite de dépôt des dossiers
 * 18 novembre : entretiens avec les candidats pré-sélectionnés (via IRC)
 * 3 décembre : annonce du candidat sélectionné&lt;/pre&gt;


&lt;p&gt;Pour toute question sur le contenu du dossier de candidature ou sur le fonctionnement de cet appel, envoyez un message à l'adresse &lt;a href=&quot;mailto:%63%6f%6e%74%61%63%74%40%70%67%64%61%79%2e%66%72&quot;&gt;contact@pgday.fr&lt;/a&gt;&lt;/p&gt;</content>
    
    

    
