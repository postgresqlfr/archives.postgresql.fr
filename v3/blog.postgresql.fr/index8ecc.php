Jusqu'ici, la réplication d'une base PostgreSQL est possible via différentes solutions &quot;externes&quot; telles que  &lt;a href=&quot;http://www.slony.info&quot;&gt;Slony&lt;/a&gt;, &lt;a href=&quot;http://www.bucardo.org&quot;&gt;Bucardo&lt;/a&gt; ou via une solution interne (technique du &lt;a href=&quot;http://docs.postgresqlfr.org/8.3/warm-standby.html#warm-standby-record&quot;&gt;log shipping&lt;/a&gt;). Cette dernière solution va être améliorée pour ajouter le « hot standby ». Les développeurs ne s'engagent pas sur une date de livraison de cette fonctionnalité mais on peut espérer qu'elle sera disponible dans la version 8.4
Le mécanisme d'export de ce « hot standby » se base sur l'envoi de chaque transaction validée. La réplication sera donc pratiquement synchrone.
Un second objectif, prévu pour la version 8.5, est de permettre l'accès en lecture seule sur les noeuds esclaves.
Cette annonce était très attendue et laisse entrevoir une réplication asynchrone d'un maitre vers plusieurs esclaves qui seraient très simple à mettre en place et qui répondra à la plupart des besoin de réplications.
Bien sûr, cette solution ne remplacera pas les systèmes de réplication plus sophistiqués comme &lt;a href=&quot;http://www.slony.info&quot;&gt;Slony&lt;/a&gt;, &lt;a href=&quot;http://www.bucardo.org&quot;&gt;Bucardo&lt;/a&gt;, &lt;a href=&quot;http://pgpool.projects.postgresql.org/&quot;&gt;pgPool&lt;/a&gt;, &lt;a href=&quot;http://pgfoundry.org/projects/skytools/&quot;&gt;Londiste&lt;/a&gt; et consorts.</content>
    
    

    
