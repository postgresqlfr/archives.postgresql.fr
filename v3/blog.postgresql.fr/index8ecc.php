    <content type="html">    &lt;p&gt;Le PGDay France est la conférence annuelle de la communauté francophone de PostgreSQL. Cette année, l’événement se tiendra le 31 mai à Lille. Une centaine de visiteurs sont attendus pour une journée d'échanges autour de PostgreSQL et de ses projets associés. Retrouvez plus d'informations sur le site de l’événement&amp;nbsp;: http://www.pgday.fr&lt;/p&gt;


&lt;p&gt;Vous êtes expert sur un domaine lié aux bases de données libres&amp;nbsp;? Vous avez utilisé PostgreSQL dans un contexte spécifique (gros volumes, forte charge, client reconnu, projet innovant, etc.)&amp;nbsp;? Vous participez à un projet libre lié à PostgreSQL&amp;nbsp;? Alors n'hésitez pas à proposer une présentation&amp;nbsp;!&lt;/p&gt;


&lt;p&gt;Pour l’édition 2016, les thèmes particulièrement mis en lumière sont les suivants&amp;nbsp;:&lt;/p&gt;

&lt;ul&gt;
&lt;li&gt;Administration de bases volumineuses&lt;/li&gt;
&lt;li&gt;Études de cas / témoignages&lt;/li&gt;
&lt;li&gt;Industrialisation&lt;/li&gt;
&lt;li&gt;Entrepôts de données&lt;/li&gt;
&lt;li&gt;Systèmes décisionnels&lt;/li&gt;
&lt;li&gt;Travaux sur la sémantique&lt;/li&gt;
&lt;li&gt;Big Data&lt;/li&gt;
&lt;li&gt;Data Mining / Exploration de Données&lt;/li&gt;
&lt;li&gt;Systèmes d'Information Géographiques&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;Cette liste n'est pas exhaustive. Il est possible de proposer d'autres sujets liés à PostgreSQL.&lt;/p&gt;


&lt;p&gt;La conférence PGDay France est à destination des professionnels, notamment les directeurs informatiques, les décideurs, les chefs de projets, les administrateurs de bases de données, les développeurs, les administrateurs systèmes et tous les profils qui entrent en contact avec un SGBD.&lt;/p&gt;


&lt;p&gt;Pour soumettre une intervention, il vous suffit d'envoyer un e-mail à l'adresse contact@pgday.fr, en précisant les éléments suivants&amp;nbsp;:&lt;/p&gt;

&lt;ul&gt;
&lt;li&gt;vos nom et prénom&lt;/li&gt;
&lt;li&gt;votre société/employeur&lt;/li&gt;
&lt;li&gt;une bio succincte (300 caractères max.)&lt;/li&gt;
&lt;li&gt;votre compte twitter (optionnel)&lt;/li&gt;
&lt;li&gt;le titre de votre intervention&lt;/li&gt;
&lt;li&gt;la durée de votre intervention (45 min. max.)&lt;/li&gt;
&lt;li&gt;une description courte (200 caractères max.)&lt;/li&gt;
&lt;li&gt;une description longue (700 caractères max.)&lt;/li&gt;
&lt;li&gt;une photo (200x200 pixels minimum)&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;Les interventions devront être en français et disponibles sous licence libre. Les interventions pourront faire l'objet d'une captation audio/vidéo et d'une diffusion sur internet.&lt;/p&gt;


&lt;p&gt;La date limite de réception des propositions est fixée au 14 février 2016 à 23h59 CEST.&lt;/p&gt;


&lt;p&gt;Dans le courant du mois de février 2016, un sondage communautaire sera organisé au sein de la communauté francophone pour évaluer les différentes propositions.&lt;/p&gt;


&lt;p&gt;Ensuite, le comité de sélection étudiera toutes les propositions valides. Le choix des sessions sera basé sur la présentation de la soumission, son intérêt pour une audience professionnelle, la cohérence du programme de la journée et sur le résultat du vote préliminaire.&lt;/p&gt;


&lt;p&gt;La décision du comité de sélection sera finale et sans appel.&lt;/p&gt;


&lt;p&gt;Les membres du comité s'expriment en leur nom propre. Leurs choix ne reflètent pas la position de leur employeur.&lt;/p&gt;


&lt;p&gt;Les orateurs sélectionnés seront avertis par e-mail avant le 7 mars 2016.&lt;/p&gt;


&lt;p&gt;Pour toute question à propos de cet appel à conférenciers et du PGDay France en général, vous pouvez envoyer un message à l'adresse&amp;nbsp;: contact@pgday.fr&lt;/p&gt;</content>
    
    

    
