    <content type="html">&lt;p&gt;&lt;/p&gt;    Pour concurrencer MySQL, qui a pris énormément de parts de marché du fait la simplicité de sa base de données, le PGDG annonce aujourd'hui la fin du support des fonctionnalités &lt;a href=&quot;http://fr.wikipedia.org/wiki/Propri%C3%A9t%C3%A9s_ACID&quot; target=&quot;_blank&quot;&gt;ACID&lt;/a&gt; dans PostgreSQL.
