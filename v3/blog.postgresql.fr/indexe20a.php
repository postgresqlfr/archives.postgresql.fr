&lt;h1 class=&quot;western&quot;&gt;3. Rapport moral présenté par le président&lt;/h1&gt;
&lt;p align=&quot;justify&quot;&gt;L'année 2005 a été la première année d'existence de l'association.
&lt;/p&gt;
&lt;p align=&quot;justify&quot;&gt;Au cours de cette année, le besoin d'une représentation francophone de PostgreSQL a été régulièrement confirmé.&lt;/p&gt;
&lt;p align=&quot;justify&quot;&gt;L'association a réalisé de nombreux projets, et participé à de nombreux évènements.&lt;/p&gt;
&lt;h2 class=&quot;western&quot; align=&quot;justify&quot;&gt;3.1. Membres&lt;/h2&gt;
&lt;p align=&quot;justify&quot;&gt;À ce jour, sans compter les adhésions enregistrées lors de SL2006, l'association compte 21 membres. Une dizaine de membres, de part leur participation active au sein de l'association et leur présence lors des différents évènements menés par l'association, représentent le noyau dur de l'association.&lt;/p&gt;
&lt;h2 class=&quot;western&quot;&gt;3.2. Actions menées&lt;/h2&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.1. SolutionsLinux2005, Paris, février 2005&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Première participation de l'association à un événement public.
&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'association a utilisé son stand pour se faire connaître du grand public. Un fort intérêt pour PostgreSQL a été ressenti tout au long du salon. De nombreux professionnels sont venus nous voir pour nous encourager.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Le salon a été l'occasion d'accueillir Jan Wieck, qui participa à l'atelier SGBD présidé par Jean-Paul Argudo. Jan Wieck a été tout au long du salon sollicité maintes fois.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'atelier SGBD a été placé par son président sous la couleur de PostgreSQL :&lt;/p&gt;
&lt;ul&gt;
&lt;ul&gt;
&lt;li&gt;Jan Wieck a présenté Slony1 et pris la place de Bruce Momjan pour présenter PostgreSQL&amp;nbsp;8.0&amp;nbsp;;
&lt;/li&gt;
&lt;li&gt;Stéphane Schildknecht a présenté un panorama des SGBD OpenSource ;
&lt;/li&gt;
&lt;li&gt;Jean-Paul Argudo a présidé la session et présenté ora2pg à la place de Gilles Darold, souffrant.
&lt;/li&gt;
&lt;/ul&gt;
&lt;/ul&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'association a reçu le deuxième prix de la meilleure jeune association.&lt;/p&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.2. Rencontres mondiales des logiciels libres, Dijon, juillet 2005&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'association était présente sur le stand de traduc.org. Guillaume Lelarge, Emmanuel Seymann, Jean-Christophe Arnu et Stéphane Schildknecht ont fait le déplacement.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Jean-Christophe Arnu et Stéphane Schildknecht ont participé à un atelier SGBD (Jean-Christophe Arnu : Exemple de mise en place de PG dans  un environnement contraint, Stéphane Schildknecht : Panorama des SGBD OpenSource et PG 8.0).&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'association était peu visible en terme marketing (pas de stand clairement identifiable) mais a communiqué sur sa participation à travers différents canaux.&lt;/p&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.3. Journées des logiciels libres, Lyon, 2 jours (date ?)&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Guillaume Lelarge, Thomas Silvi, Jean-Paul Argudo y ont tenu un stand aux couleurs de l'association (Affiches A3). Etaient présentes à ce salon exclusivement des associations du monde libre.&lt;/p&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.5. Dijon, ESIREM, décembre 2005&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Stéphane Schildknecht a dispensé une formation d'une journée aux étudiants de l'ESIREM, dans le cadre de leur formation aux SGBD.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;La journée s'est découpée comme suit :&lt;/p&gt;
&lt;ul&gt;
&lt;ul&gt;
&lt;li&gt;panorama des SGBD OpenSources ;
&lt;/li&gt;
&lt;li&gt;bases de données et haute-disponibilité ;
&lt;/li&gt;
&lt;li&gt;présentation de PostgreSQL ;
&lt;/li&gt;
&lt;li&gt;présentation de PostgreSQLFr et PostgreSQLFr.org ;
&lt;/li&gt;
&lt;li&gt;questions – réponses autour de l'entrée prochaine des étudiants dans le monde du travail.
&lt;/li&gt;
&lt;/ul&gt;
&lt;/ul&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Les étudiants étaient réceptifs, autant au modèle libre qu'à l'existence d'une concurrence au tout-propriétaire en matière de gestion des données.&lt;/p&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.6. OpenDB CON&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Jean-Paul Argudo s'est rendu à l'OpenDatabaseConference, à Frankfurt.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Cela lui a permis de rencontrer Gavin Sherry, Josh Berkus, Peter Eisentraut. Les membres de PostgreSQL ont montré beaucoup d'intérêt pour notre association. En revanche, ils estiment que l'association ne peut pas être aidée financièrement par la communauté, mais doit devenir rapidement indépendante.&lt;/p&gt;
&lt;h3 class=&quot;western&quot;&gt;3.2.7. SolutionsLinux2006&lt;/h3&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;L'association tient un stand sur le salon. Un peu plus de volontaires ont répondu que l'année précédente. L'association a prévu de vendre des T-shirts, de diffuser un prospectus vantant les mérites de l'association et de PostgreSQL.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Des autocollants ont également été réalisés.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Par l'entremise de Jean-Paul Argudo et de Gavin Sherry, invité de PostgreSQLFr, l'association a participé cette année encore à l'atelier SGBD.&lt;/p&gt;
&lt;p style=&quot;margin-left: 1.25cm;&quot;&gt;Gavin Sherry a présenté les concepts de Slony2.&lt;/p&gt;
&lt;h2 class=&quot;western&quot;&gt;3.3. Evolutions techniques&lt;/h2&gt;
&lt;p align=&quot;justify&quot;&gt;Jean-Paul Argudo rappelle que pendant la première partie de 2005, le site internet était hébergé sur un «&amp;nbsp;OpenBrick&amp;nbsp;» chez Lost-Oasis.&lt;/p&gt;
&lt;p align=&quot;justify&quot;&gt;Pour la seconde partie de 2005, l'hébergement s'est professionnalisé. Il est en effet assuré par Dalibo, qui a pris en charge également l'hébergement des comptes courriels de l'association.
&lt;/p&gt;
