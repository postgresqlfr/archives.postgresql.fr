    <content type="html">    &lt;p&gt;Bonjour à tous,&lt;/p&gt;


&lt;p&gt;&lt;ins&gt;&lt;strong&gt;Objectifs&lt;/strong&gt;&lt;/ins&gt;&lt;/p&gt;


&lt;p&gt;Le but premier de l'association PostgreSQLFr est a promotion du SGBD PostgreSQL. À ce titre, les membres de l'association lancent un programme de soutien aux bénévoles qui présentent PostgreSQL et son écosystème à de nouveaux publics.&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;Présenter PostgreSQL dans une université ou une école d'ingénieurs&lt;/li&gt;
&lt;li&gt;Donner une conférence à l'occasion des RMLL&lt;/li&gt;
&lt;li&gt;Intervenir auprès d'une autre communauté (PHP, Java, Python, …)&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;Le principe est simple&amp;nbsp;: L'association prend à sa charge tout ou partie des frais de déplacement, d'hébergement et de restauration de l'intervenant.
Pour l'année 2012, le budget global maximum attribué à de programme est de 600 € TTC.&lt;/p&gt;


&lt;p&gt;&lt;ins&gt;&lt;strong&gt;Actions éligibles&lt;/strong&gt;&lt;/ins&gt;&lt;/p&gt;


&lt;p&gt;Pour chaque demande de soutien, les membres de l'association sont consultés et se prononcent pour déterminer si l'action est éligible ou pas.
Pour être acceptée, l'intervention doit respecter les critères suivants&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;PostgreSQL ou un outil satellite (PostGIS / pgAdmin / etc.) est le sujet principal de la présentation&lt;/li&gt;
&lt;li&gt;La présentation est en français&lt;/li&gt;
&lt;li&gt;L'orateur n'est pas rémunéré pour sa présentation&lt;/li&gt;
&lt;li&gt;L'orateur n'est pas défrayé par un autre organisme pour sa présentation&lt;/li&gt;
&lt;li&gt;La présentation ne fait pas la promotion d'une société commerciale&lt;/li&gt;
&lt;li&gt;Les documents de présentation (“slides”) sont disponibles sous licence CC-BY-SA ou compatible&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;De plus, l'objectif est clairement d'atteindre de nouveaux publics. Par conséquent, les événements de la communauté PostgreSQL (PG Con, PG Open, PG Conference Europe, PG Day France, PG Sessions, etc. ) ne sont pas éligibles.&lt;/p&gt;


&lt;p&gt;&lt;ins&gt;&lt;strong&gt;Dépenses éligibles&lt;/strong&gt;&lt;/ins&gt;&lt;/p&gt;


&lt;p&gt;L'association peut rembourser les frais suivants&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;1 aller-retour entre le domicile et le lieu de la présentation (de préférence en train 2nde classe)&lt;/li&gt;
&lt;li&gt;1 nuit d’hôtel (de préférence en hôtel 2 étoiles)&lt;/li&gt;
&lt;li&gt;1 repas complet&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;L'association rembourse les frais uniquement sur présentation des originaux (factures, billets, notes de restaurant).
Le plafond maximal des remboursements est fixé à 250 € TTC par intervention. Le programme est limité à 1 remboursement par personne et par an.&lt;/p&gt;


&lt;p&gt;&lt;ins&gt;&lt;strong&gt;Comment procéder ?&lt;/strong&gt;&lt;/ins&gt;&lt;/p&gt;


&lt;p&gt;Toute personne peut demander un remboursement de ses frais. Pour cela, il suffit d'envoyer au préalable un message à bureau@postgresql.fr en précisant&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;Le nom et prénom de l'intervenant&lt;/li&gt;
&lt;li&gt;L'heure et le lieu de la présentation&lt;/li&gt;
&lt;li&gt;Le contact de quelqu'un qui organise l’événement&lt;/li&gt;
&lt;/ul&gt;
&lt;pre&gt;* La description de l'intervention ( durée, public, etc. )
* Une estimation des frais qui seront engagés&lt;/pre&gt;


&lt;p&gt;La demande sera ensuite présentée publiquement aux membres de l'association et une réponse sera émise sous 15 jours.
Si la réponse est positive et une fois la présentation effectuée, l'orateur pourra obtenir le remboursement de ses frais en présentant les éléments suivant&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;Le justificatif de chaque dépense&lt;/li&gt;
&lt;li&gt;Les slides de la présentation&lt;/li&gt;
&lt;li&gt;Un bref compte-rendu de son intervention&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;&lt;ins&gt;&lt;strong&gt;Validité&lt;/strong&gt;&lt;/ins&gt;&lt;/p&gt;


&lt;p&gt;Cette opportunité est valable du 1er septembre 2012 au 31 décembre 2012. Si l'expérience est concluante, elle sera reconduite en 2013.&lt;/p&gt;</content>
    
    

    
