&lt;p&gt;PostgreSQLFr a signé la pétition DADVSI, et a placé en page d'accueil un lien et un bandeau invitant les visiteurs a se mobiliser contre ce projet de loi.&lt;/p&gt;
&lt;p&gt;Une bannière EUCD a également été placé en page d'accueil pour soutenir la lutte contre les brevets logiciels. La suite nous a donnée raison.&lt;/p&gt;
