&lt;h2 class=&quot;western&quot;&gt;3.4. Réalisations commerciales&lt;/h2&gt;
&lt;p&gt;Afin d'assurer une visibilité réelle à l'association, celle-ci a procédé à la réalisation de plaquettes de présentation de l'association et du produit, de T-shirts, financés par Dalibo, d'autocollants et de trois affiches, l'une au format A0, les 2 autres au format A1.&lt;/p&gt;
&lt;h2 class=&quot;western&quot;&gt;3.5. Site internet&lt;/h2&gt;
&lt;p&gt;On note une très forte fréquentation du site, en continuelle progression.&lt;/p&gt;
&lt;p&gt;Le site reçoit en moyenne 200 000 hits par mois, ce qui représente environ 2Go de bande passante. Plus de 1300 personnes sont enregistrées sur le site. Il serait intéressant de savoir combien d'entre elles sont identifiées de manière unique.&lt;/p&gt;
&lt;p&gt;Chaque récit publié sur le site est lu entre 150 et 300 fois.&lt;/p&gt;
