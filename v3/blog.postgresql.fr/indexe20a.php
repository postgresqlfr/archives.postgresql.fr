&lt;/li&gt;
&lt;li&gt;pour joindre le noyau dur : &lt;a href=&quot;mailto:equipe@postgresqlfr.org&quot;&gt;equipe@postgresqlfr.org&lt;/a&gt;
&lt;/li&gt;
