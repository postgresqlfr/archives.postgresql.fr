    <content type="html">    &lt;p&gt;Bonjour,&lt;/p&gt;


&lt;p&gt;L'été touche bientôt à sa fin, et les enfants ont repris le chemin de l'école. Leurs parents aussi on repris le travail, et après l'accalmie habituelle de l'été, la vie reprend un peu partout. Mais, ce n'est pas la rentrée pour tout le monde, le retour de vacances ne s'applique qu'à ceux qui en ont pris, et il y a les autres, ceux qui ont travaillé tout l'été pour payer leurs études, leurs projets, ou parce qu'ils avaient des obligations.&lt;br /&gt; Et puis il y a ceux qui ont travaillé sur leur logiciel préféré et qui vont bientôt nous sortir la version 9.0&lt;/p&gt;


&lt;p&gt;Oui, PostgreSQL 9.0 approche à grands pas. La RC1 est disponible depuis tout juste une semaine, et l'on parle déjà de la sortie imminente de la version finale, si &quot;tout va bien&quot; ou plutôt si rien ne va mal.&lt;/p&gt;


&lt;p&gt;En parallèle de cette future sortie, l'association a un certain nombre de projets en cours et qui ont besoin d'avancer. Que ce soient le PAP (appel à projets), la préparation de la sortie de PG 9.0 (avec un délai probablement court), ou la préparation d'un PGDay francophone, le travail ne manque pas. Sans compter les chantiers internes à l'association comme la réécriture des statuts et du règlement intérieur ou le déménagement à Paris.&lt;br /&gt;
Mais avant de voir le détail de ces points, passons à la nouveauté&amp;nbsp;:  la présentation d'un membre de l'association.&lt;/p&gt;


&lt;p&gt;Bonne lecture!&lt;/p&gt;



&lt;h2&gt;Présentation de Vincent Picavet&lt;/h2&gt;

&lt;p&gt;Je m'appelle Vincent Picavet et je suis ingénieur en mathématiques appliquées et expert en systèmes d'information géographiques OpenSource.
Depuis longtemps convaincu par le logiciel libre, je suis passé à l'INRIA et Makina Corpus,dans le libre et les SIG, avant de créer Oslandia en 2009.&lt;br /&gt;
J'ai plongé avec plaisir dans PostgreSQL avec la découverte de PostGIS il y a quelques années. Dans les architectures de SIG, les systèmes de stockage et d'analyse des données géographiques sont un élément clé, et le couple PostgreSQL/PostGIS répond particulièrement bien à ce besoin.&lt;br /&gt;
Je me suis donc construit une expertise et ai contribué à ce produit, jusqu'à créer Oslandia avec Olivier Courtin qui est commiter PostGIS.&lt;br /&gt;
Autour de PostgreSQL et de PostGIS, j'ai donc l'occasion de faire de la formation, de l'audit et du développement, que ce soit sur PostGIS ou sur des modules de PostgreSQL autour des problématiques SIG. La souplesse de PostgreSQL permet de l'adapter à des problèmes et contextes spécifiques rencontrés dans le domaine des SIG, ce qui en fait réellement une solution de choix.&lt;br /&gt;
Je fais aussi beaucoup de communication autour de cette base de données spatiale&amp;nbsp;: PGDay.eu, FOSS4G, Solutions Linux, Rencontres SIG la lettre…&lt;br /&gt;
Et j'essaie également de donner un coup de main à PostgreSQL-fr sur les évènements auxquels elle participe. Cela me donne l'occasion de rencontrer ses membres autour d'un verre, ce qui est toujours un plaisir et permet de saisir de façon plus précise l'importance que prend PostgreSQL dans le paysage informatique.&lt;/p&gt;


&lt;p&gt;Pour finir, quelques signes caractéristiques&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;vi vs emacs&amp;nbsp;: vi&lt;/li&gt;
&lt;li&gt;gnome vs kde&amp;nbsp;: kde&lt;/li&gt;
&lt;li&gt;Linux vs mac vs *BSD&amp;nbsp;: Ubuntu@home, Debian@serveurs&lt;/li&gt;
&lt;li&gt;psql vs pgadmin&amp;nbsp;: les 2 mon capitaine&lt;/li&gt;
&lt;li&gt;PostgreSQL vs Oracle vs Oracle MySQL&amp;nbsp;: PostgreSQL only&amp;nbsp;!&lt;/li&gt;
&lt;li&gt;Reply-to-list ou reply-to-user&amp;nbsp;: no comment&lt;/li&gt;
&lt;/ul&gt;
&lt;pre&gt;&lt;/pre&gt;

&lt;hr /&gt;


&lt;p&gt;Chaque mois, un membre volontaire de l'association est invité à se présenter et à parler de ses activités avec PostgreSQL.&lt;/p&gt;


&lt;h2&gt;Réunion de travail&lt;/h2&gt;

&lt;p&gt;En déplacement professionnel sur la région parisienne, Jean-Christophe propose une réunion de travail des membres qui seront disponibles.&lt;br /&gt;
Cette réunion aurait lieu le 16 septembre dans la soirée, dans un lieu qui sera précisé en début de semaine.&lt;/p&gt;


&lt;h2&gt;Projets en cours&lt;/h2&gt;

&lt;h3&gt;Proposition d'Appel à Projets&lt;/h3&gt;

&lt;p&gt;Rappel&amp;nbsp;: lors des élections l'appel à proposition de Damien Clochard a été approuvé.&lt;br /&gt;
Une première version de l'appel se trouve sur le wiki&amp;nbsp;: &lt;a href=&quot;http://www.postgresql.fr/appel_a_projets_2010:appel&quot; title=&quot;http://www.postgresql.fr/appel_a_projets_2010:appel&quot;&gt;http://www.postgresql.fr/appel_a_pr...&lt;/a&gt;.&lt;br /&gt;
Le travail est coordonné via la liste des membres, et a besoin de monde pour se concrétiser.&lt;br /&gt;
L'appel à projet est maintenant dans son état final, il faut le diffuser le plus largement possible.&lt;br /&gt;
Damien a initié une page sur le wiki pour pointer les différents lieux de diffusion&amp;nbsp;: &lt;a href=&quot;http://www.postgresql.fr/appel_a_projets_2010:diffusion&quot; title=&quot;http://www.postgresql.fr/appel_a_projets_2010:diffusion&quot;&gt;http://www.postgresql.fr/appel_a_pr...&lt;/a&gt;.&lt;/p&gt;


&lt;h3&gt;PGDayFR&lt;/h3&gt;

&lt;p&gt;Rappel&amp;nbsp;: une réunion IRC concernant ce projet a eu lieu le 2 juin pour faire le point sur les lieux.&lt;br /&gt;
Le constat est qu'il n'y a pas assez de ressources volontaires pour mener à bien ce projet.&lt;br /&gt;
Nous proposons de reporter cet évènement à 2011.&lt;/p&gt;


&lt;h3&gt;Réécriture des statuts et du règlement intérieur&lt;/h3&gt;

&lt;p&gt;Un chantier de réécriture des statuts de l'association est en cours et devrait bientôt passer une nouvelle étape.&lt;/p&gt;


&lt;h3&gt;Siège de l'association&lt;/h3&gt;

&lt;p&gt;L'association a prévu de déménager à Paris. Les procédures ne sont pas finalisées et attendent également les nouveaux statuts.&lt;/p&gt;


&lt;h3&gt;PG9.0 National Tour&lt;/h3&gt;

&lt;p&gt;Rappel&amp;nbsp;: le principe de ce projet est d'écrire à plusieurs une présentation de PostgreSQL 9.0. Cette présentation pourra être utilisée lors de conférences que nous pourrions faire partout en France (en fonction de nos localisations) en sollicitant les LUGs locaux.&lt;/p&gt;


&lt;p&gt;Le projet n'a pas avancé depuis juin.&lt;br /&gt;
Comme la sortie de Pg 9.0 approche à grands pas, il est important de relancer ce sujet.&lt;/p&gt;


&lt;h2&gt;Point mensuel&lt;/h2&gt;

&lt;p&gt;Chaque mois, l'association PostgreSQLFr envoi ce bulletin mensuel à destination des membres.&lt;br /&gt;
Il a pour objectif de tenir tout le monde informé de manière synthétique sur les actions menées au sein de l'association.&lt;br /&gt;
Vous pouvez participer en envoyant les informations que vous souhaitez voir figurer par l'alias de courriel &quot;bureau&quot;.&lt;/p&gt;


&lt;p&gt;Ce bulletin est diffusé dans deux version&amp;nbsp;: l'une à destination des membres, l'autre (celle-ci) pour une diffusion plus large à l'extérieur de l'association.&lt;/p&gt;</content>
    
    

    
