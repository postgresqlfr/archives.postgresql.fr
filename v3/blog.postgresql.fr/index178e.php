    <content type="html">&lt;p&gt;Avec la sortie de Solaris 10 update 2, Sun a annoncé aujourd'hui
l'inclusion de PostgreSQL dans Solaris et la disponibilité du support
PostgreSQL (&lt;a href=&quot;http://sun.com/solaris/postgresql.jsp&quot; target=&quot;_blank&quot;&gt;http://sun.com/solaris/postgresql.jsp&lt;/a&gt;).&lt;/p&gt;
&lt;p&gt;Cette
offre donne aux utilisateurs de Solaris un accès direct à un SGBDR à
haute performances, et la confiance du support Sun mondial pour le
support de leurs applications de production. Les utilisateurs de
PostgreSQL peuvent prendre cela comme une opportunité pour ré-évaluer
la plate-forme Solaris pour leurs projets où la robustesse est un but
principal.&lt;/p&gt;    &lt;p&gt;Les ingénieurs de Sun dédiés aux bases de données et aux performances, dirigés par Josh Berkus (membre de la core team PostgreSQL), travailleront les prochains mois pour améliorer la version de PostgreSQL pour Solaris, améliorer les performances, et ajouter de nouvelles fonctionalités. 100% de ces améliorations seront Open-Source et données à la communauté PostgreSQL ou dans d'autres projets associés.&lt;/p&gt;
&lt;p&gt;Le support PostgreSQL de Sun pour Solaris commence à 1596 dollars américains par an, pour le monde entier.&lt;/p&gt;
