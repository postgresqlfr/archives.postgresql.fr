    <content type="html">&lt;p&gt;La version 1.2 de Benetl est sortie.&lt;/p&gt;
&lt;p&gt;Elle est disponible gratuitement sur :
&lt;a href=&quot;http://www.benetl.net&quot;&gt;http://www.benetl.net&lt;/a&gt;
&lt;/p&gt;
&lt;p&gt;
Elle apporte de nouvelles fonctionnalités, notamment la possibilité de générer des scripts d'insertion en base sans s'appuyer sur le MCD propre de Benetl (il est possible de choisir le nom d'une table). Cela fait de Benetl un loader évolué pour postgreSQL.&lt;/p&gt;    Le MCD de Benetl a évolué pour permettre l'association de plusieurs entités au sein de groupes d'entités. Cela renforce les possibilités de calculs sur des lots de données.
