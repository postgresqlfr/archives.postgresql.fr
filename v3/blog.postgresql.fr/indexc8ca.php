    <title>Appel à soutien : postgresql.fr a besoin de 2 vm Linux</title>
    <link href="http://blog2.postgresql.fr/index.php?post/2016/06/30/Appel-%C3%A0-soutien-%3A-postgresql.fr-a-besoin-de-2-vm-Linux" rel="alternate" type="text/html"
    title="Appel à soutien : postgresql.fr a besoin de 2 vm Linux" />
    <id>urn:md5:161379db3927fb0bc7f7f7caedf180c8</id>
    <published>2016-06-30T09:22:00+01:00</published>
    <updated>2016-06-30T08:23:02+01:00</updated>
    <author><name>daamien</name></author>
        <dc:subject>Vie du site</dc:subject>
            
    <content type="html">    &lt;p&gt;Bonjour à toutes et à tous,&lt;/p&gt;


&lt;p&gt;Entre 2012 et 2015, la plateforme postgresql.fr a été hébergée à titre gracieux par la société Gandi. Aujourd'hui Gandi souhaite mettre un terme à ce soutien et nous les remercions chaleureusement pour leur aide pendant les 3 dernières années.&lt;/p&gt;


&lt;p&gt;L'équipe des administrateurs postgresql.fr prépare donc une migration de ces services et pour ce faire nous sommes à la recherche de deux serveurs linux virtuels ayant chacun la configuration suivante&amp;nbsp;:&lt;/p&gt;

&lt;ul&gt;
&lt;li&gt;50 Go d'espace disque;&lt;/li&gt;
&lt;li&gt;4 CPU virtuels&amp;nbsp;;&lt;/li&gt;
&lt;li&gt;1Go de RAM&amp;nbsp;;&lt;/li&gt;
&lt;li&gt;une bande passante illimitée&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;Évidement ça peut être plus :-)&lt;/p&gt;


&lt;p&gt;Nous souhaitons par ailleurs les conditions d'hébergement suivantes&amp;nbsp;:&lt;/p&gt;

&lt;ul&gt;
&lt;li&gt;4 adresses IPv4 publiques;&lt;/li&gt;
&lt;li&gt;Un système d'exploitation GNU/Linux (Debian a notre préférence);&lt;/li&gt;
&lt;li&gt;Un accès direct pour redémarrer les machines virtuelles nous-même;&lt;/li&gt;
&lt;li&gt;Une sauvegarde quotidienne des machines;&lt;/li&gt;
&lt;li&gt;Un moyen simple (email, téléphone) et direct de contacter les administrateurs de la plateforme (si possible 7jour/7 et de préférence en français).&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;L'équipe souhaite par ailleurs signer une convention pour assurer la disponibilité et la gratuité des machines virtuelles pendant une durée de 1 à 3 ans.&lt;/p&gt;


&lt;p&gt;Pour rappel, la plateforme postgresql.fr c'est&amp;nbsp;:&lt;/p&gt;

&lt;ul&gt;
&lt;li&gt;le site postgresql.fr, son blog et son forum;&lt;/li&gt;
&lt;li&gt;la documentation de PostgreSQL en français;&lt;/li&gt;
&lt;li&gt;la documentation de Slony en français;&lt;/li&gt;
&lt;li&gt;le site du PG Day France;&lt;/li&gt;
&lt;li&gt;le service mail du domaine postgresql.fr et autres;&lt;/li&gt;
&lt;li&gt;les services web de l'association postgresqlfr;&lt;/li&gt;
&lt;li&gt;la planète des blogs postgresql francophones;&lt;/li&gt;
&lt;li&gt;d'autres choses qui m'échappent au moment où j'écris ces lignes :)&lt;/li&gt;
&lt;/ul&gt;

&lt;p&gt;Donner une machine virtuelle à l'équipe postgresql.fr c'est donc aider activement l'ensemble de ces projets et de manière générale soutenir la communauté francophone de PostgreSQL...&lt;/p&gt;


&lt;p&gt;Si vous disposez des moyens pour nous fournir tout ou partie de cette infrastructure, merci de nous contacter sur admin@postgresql.fr&lt;/p&gt;


&lt;p&gt;Bonne journée&amp;nbsp;!&lt;/p&gt;


&lt;p&gt;Damien Clochard pour l'équipe des admins de postgresql.fr&lt;/p&gt;




&lt;p&gt;''
PS&amp;nbsp;: Je précise que l'équipe postgresql.fr est autonome, bénévole et ouverte aux bonnes volontés. Nous n'avons pas compte bancaire et refusons les dons en espèce ou via paypal.&lt;/p&gt;


&lt;p&gt;Nous n'avons pas besoin d'argent. Nous avons besoin de bénévoles et de matériel ;-)''&lt;/p&gt;</content>
    
    

    
