    <title>Version PDF du manuel français grandement améliorée</title>
    <link href="http://blog2.postgresql.fr/index.php?post/drupal/391" rel="alternate" type="text/html"
    title="Version PDF du manuel français grandement améliorée" />
    <id>urn:md5:dfdd9872961af575d2c40f186881c560</id>
    <published>2006-05-01T12:29:00+00:00</published>
    <updated>2008-10-14T16:11:14+00:00</updated>
    <author><name>gleu</name></author>
        <dc:subject>Vie du site</dc:subject>
            
    <content type="html">&lt;p&gt;&lt;/p&gt;    &lt;p&gt;La version PDF de la documentation n'a jamais été bien réalisée. Ceci est principalement dû à la façon dont elle était construite. L'équipe de traduction utilisait openjade pour générer un gros fichier HTML contenant toute la documentation. Puis, ce document était fournit à &lt;a href=&quot;http://www.htmldoc.org&quot;&gt;htmldoc&lt;/a&gt; qui créait le PDF... sans prendre en compte les feuilles de style (une des grosses limitations de la version 1.8 de ce logiciel). De plus, ce logiciel a peu d'options qui permettraient d'améliorer la présentation du PDF.&lt;/p&gt;
&lt;p&gt;Comme certaines personnes (nommément fabix et Kryskool) souhaitaient passer en DocBook XML, le passage a été entrepris. Cela a permis d'utiliser une chaîne d'outils bien plus complète et flexible. Le passage au XML et les modifications nécessaires pour créer un PDF agréable à utiliser et à lire ont pris entre deux et trois semaines... mais le résultat est plus que probant.&lt;/p&gt;
&lt;p&gt;L'équipe de traduction vous fournit donc un &lt;a href=&quot;http://docs.postgresqlfr.org/pgsql-8.1.3-fr/pg813.pdf&quot; title=&quot;Manuel PostgreSQL de la version 8.1.3 au format PDF&quot;&gt;PDF de grande qualité&lt;/a&gt; qui semble &lt;a href=&quot;http://www.traduc.org/%7Egleu/dotclear/index.php?2006/04/29/501-postgresql-manual-in-pdf#c1268&quot;&gt;faire envie à certaines personnes utilisant la version originale&lt;/a&gt; :) À ce sujet, il est évident qu'un travail sera entrepris pour améliorer le PDF de la version originale.&lt;/p&gt;
&lt;p&gt;Dernière information sur la traduction, il existe maintenant plusieurs sites pour la traduction&amp;nbsp;:&lt;/p&gt;
&lt;ul&gt;
&lt;li&gt;le &lt;a href=&quot;http://docs.postgresqlfr.org/&quot; title=&quot;docs.postgresqlfr.org, le site des utilisateurs&quot;&gt;site des utilisateurs&lt;/a&gt; ;&lt;/li&gt;
&lt;li&gt;et &lt;a href=&quot;http://svn.postgresqlfr.org/&quot; title=&quot;svn.postgresqlfr.org, le site des développeurs&quot;&gt;celui des développeurs&lt;/a&gt;.&lt;/li&gt;
&lt;/ul&gt;
&lt;p&gt;En cas de problème avec la documentation, n'hésitez pas à saisir un rapport sur le site des développeurs&amp;nbsp;!&lt;/p&gt;
&lt;p&gt;Attention, ce PDF ne sera disponible que pour les versions 8.1.3 et ultérieures.&lt;/p&gt;</content>
    
    

    
